/* eslint-disable no-undef */
import dotenv from "dotenv";
dotenv.config();

import cors from "cors";
import express from "express";
import morgan from "morgan";
import nodemailer from "nodemailer";
import axios from "axios";
import rateLimit from "express-rate-limit";
import multer from "multer";


const app = express();

// ─── Middleware ──────────────────────────────────────────────────────────────
app.use(cors());
app.use(morgan("dev"));
app.use(express.json());
app.use(express.urlencoded({ extended: true }));

// ─── Rate Limiting (max 5 email requests per 15 min per IP) ─────────────────
const emailRateLimiter = rateLimit({
  windowMs: 15 * 60 * 1000,
  max: 5,
  standardHeaders: true,
  legacyHeaders: false,
  message: {
    message: "Too many requests from this IP, please try again after 15 minutes.",
  },
});

// ─── Reusable Nodemailer Transporter ────────────────────────────────────────
const transporter = nodemailer.createTransport({
  service: "gmail",
  secure: true,
  auth: {
    user: process.env.USER_AUTH_EMAIL,
    pass: process.env.USER_AUTH_PASSWORD,
  },
});

transporter.verify((error) => {
  if (error) {
    console.error("❌ Mail transporter error:", error.message);
  } else {
    console.log("✅ Mail transporter is ready");
  }
});

// ─── Helpers ─────────────────────────────────────────────────────────────────
/**
 * Verifies a Google reCAPTCHA token.
 * @returns {boolean} true if valid
 */
async function verifyCaptcha(token) {
  const url = `https://www.google.com/recaptcha/api/siteverify?secret=${process.env.CAPTCHA_SECRET_KEY}&response=${token}`;
  const { data } = await axios.post(url);
  return data.success === true;
}

/**
 * Strips HTML tags from user-supplied strings to prevent injection.
 */
function sanitize(str = "") {
  return String(str).replace(/<[^>]*>/g, "").trim();
}

