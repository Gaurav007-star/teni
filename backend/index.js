// ─── Route 1: Contact Form (/api/sendmail) ───────────────────────────────────
app.post("/api/sendmail", emailRateLimiter, async (req, res) => {
  const { name, email, phone, subject, message, captchaKey } = req.body;

  // Validation
  if (!name || !email || !subject || !message) {
    return res.status(400).json({ message: "Please fill in all required fields." });
  }

  if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
    return res.status(400).json({ message: "Invalid email address." });
  }

  if (process.env.CAPTCHA_SECRET_KEY && captchaKey) {
    try {
      const captchaOk = await verifyCaptcha(captchaKey);
      if (!captchaOk) {
        return res.status(400).json({ message: "CAPTCHA verification failed. Please try again." });
      }
    } catch {
      return res.status(500).json({ message: "Could not verify CAPTCHA. Please try again later." });
    }
  }

  // Sanitize inputs
  const safe = {
    name: sanitize(name),
    email: sanitize(email),
    phone: sanitize(phone || ""),
    subject: sanitize(subject),
    message: sanitize(message),
  };

  const mailOptions = {
    from: `"TENI Website" <${process.env.USER_AUTH_EMAIL}>`,
    to: process.env.USER_RECEIVER_AUTH_EMAIL,
    replyTo: safe.email,
    subject: `[Contact] ${safe.subject} — from ${safe.name}`,
    text: `Name: ${safe.name}\nEmail: ${safe.email}\nPhone: ${safe.phone || "N/A"}\nSubject: ${safe.subject}\n\nMessage:\n${safe.message}`,
    html: contactEmailHtml(safe),
  };

  try {
    await transporter.sendMail(mailOptions);
    console.log(`✅ Contact email sent from ${safe.email}`);
    return res.status(200).json({ message: "Message sent successfully!" });
  } catch (error) {
    console.error("❌ Contact email error:", error.message);
    return res.status(500).json({ message: "Failed to send message. Please try again later." });
  }
});

// ─── Multer (in-memory storage for CV file attachments) ────────────────────
const upload = multer({
  storage: multer.memoryStorage(),
  limits: { fileSize: 5 * 1024 * 1024 }, // 5 MB
  fileFilter: (_req, file, cb) => {
    const allowed = [".pdf", ".doc", ".docx"];
    const ext = file.originalname.toLowerCase().slice(file.originalname.lastIndexOf("."));
    if (allowed.includes(ext)) {
      cb(null, true);
    } else {
      cb(new Error("Only PDF, DOC, and DOCX files are allowed."));
    }
  },
});

// ─── Route 2: CV / Job Application Form (/api/sendcv) ───────────────────────
app.post("/api/sendcv", emailRateLimiter, upload.single("cv"), async (req, res) => {
  // Expect multipart/form-data with optional file attachment handled by frontend
  const { name, email, phone, position, note, captchaKey } = req.body;

  // Validation
  if (!name || !email || !position) {
    return res.status(400).json({ message: "Name, email, and position are required." });
  }

  if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
    return res.status(400).json({ message: "Invalid email address." });
  }

  if (process.env.CAPTCHA_SECRET_KEY && captchaKey) {
    try {
      const captchaOk = await verifyCaptcha(captchaKey);
      if (!captchaOk) {
        return res.status(400).json({ message: "CAPTCHA verification failed. Please try again." });
      }
    } catch {
      return res.status(500).json({ message: "Could not verify CAPTCHA. Please try again later." });
    }
  }

  // Sanitize inputs
  const safe = {
    name: sanitize(name),
    email: sanitize(email),
    phone: sanitize(phone || ""),
    position: sanitize(position),
    note: sanitize(note || ""),
  };

  const mailOptions = {
    from: `"TENI Website" <${process.env.USER_AUTH_EMAIL}>`,
    to: process.env.USER_RECEIVER_AUTH_EMAIL,
    replyTo: safe.email,
    subject: `[CV Application] ${safe.name} — ${safe.position}`,
    text: `Applicant: ${safe.name}\nEmail: ${safe.email}\nPhone: ${safe.phone || "N/A"}\nPosition: ${safe.position}\n\nNote:\n${safe.note || "N/A"}`,
    html: cvEmailHtml(safe),
    attachments: req.file
      ? [{
        filename: req.file.originalname,
        content: req.file.buffer,
        contentType: req.file.mimetype,
      }]
      : [],
  };

  try {
    await transporter.sendMail(mailOptions);
    console.log(`✅ CV application email received from ${safe.email} for "${safe.position}"${req.file ? ` (with attachment: ${req.file.originalname})` : ""}`);
    return res.status(200).json({ message: "Application submitted successfully!" });
  } catch (error) {
    console.error("❌ CV email error:", error.message);
    return res.status(500).json({ message: "Failed to submit application. Please try again later." });
  }
});

// ─── Health Check ────────────────────────────────────────────────────────────
app.get("/health", (_req, res) => {
  res.status(200).json({ status: "ok", time: new Date().toISOString() });
});

